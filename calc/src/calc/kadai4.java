package calc;

public class kadai4 {

	public static void main(String[] args) {
		// int型の数値19287について
        int number=192837;
        
        //一の位を除いた数を出力（/演算子を用いること）
        System.out.println("一の位を除いた数："+number/10);
        
        //一の位だけの数だけを出力（%演算子を用いること）
        System.out.println("一の位のみの数："+number%10);
	}

}
